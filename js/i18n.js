function swapText(lang){
  const w=document.createTreeWalker(document.body,NodeFilter.SHOW_TEXT);
  let n;
  while(n=w.nextNode()){
    if(n.parentElement&&(n.parentElement.tagName==='SCRIPT'||n.parentElement.tagName==='STYLE'))continue;
    if(n.__o===undefined)n.__o=n.nodeValue;
    const base=n.__o.trim();
    if(!base)continue;
    if(lang==='en'){n.nodeValue=n.__o;continue}
    const t=M[base];
    if(t)n.nodeValue=n.__o.replace(base,t[IDX[lang]]);
    else n.nodeValue=n.__o;
  }
  document.querySelectorAll('[placeholder]').forEach(el=>{
    if(el.__o===undefined)el.__o=el.getAttribute('placeholder');
    const t=P[el.__o];
    el.setAttribute('placeholder',lang==='en'||!t?el.__o:t[IDX[lang]]);
  });
}
